#define WINDOW_WIDTH 640
#define WINDOW_HEIGHT 360
#define FPS 60

double target_frame_time=1.0/FPS;
SDL_Window *window;
SDL_Renderer *sdl_renderer;
SDL_Texture *frame_texture;
uint32_t pixels[WINDOW_WIDTH*WINDOW_HEIGHT];
uint64_t frame_start_time;
uint64_t frame_end_time;
bool termination_requested=false;

void fw_init(const char* window_name){
	SDL_Init(SDL_INIT_VIDEO);
	window=SDL_CreateWindow(window_name,640,360,0);
	sdl_renderer=SDL_CreateRenderer(window,NULL);
	frame_texture=SDL_CreateTexture(
		sdl_renderer,
		SDL_PIXELFORMAT_RGBA8888,
		SDL_TEXTUREACCESS_STREAMING,
		WINDOW_WIDTH,WINDOW_HEIGHT 
	);
  SDL_SetTextureScaleMode(frame_texture,SDL_SCALEMODE_NEAREST);
}

void fw_close(){

};

void begin_frame(){
	//Get frame start time.
	frame_start_time=SDL_GetPerformanceCounter();

	//Poll for close event.
	SDL_Event event;
	while(SDL_PollEvent(&event)){
		if(event.type==SDL_EVENT_QUIT)termination_requested=true;
	}

	//Clear background.
	for(int i=0;i<640*360;i++)pixels[i]=0x000000FF;
}

void end_frame(){
	SDL_UpdateTexture(
    frame_texture,
    NULL,
    pixels,
    WINDOW_WIDTH*sizeof(uint32_t)
	);
	SDL_RenderClear(sdl_renderer);
	SDL_RenderTexture(sdl_renderer,frame_texture,NULL,NULL);
	SDL_RenderPresent(sdl_renderer);

	//Create framerate capping.
	frame_end_time=SDL_GetPerformanceCounter();
	double elapsed=(double)(frame_end_time-frame_start_time)/SDL_GetPerformanceFrequency();
	double remaining=target_frame_time-elapsed;
	if(remaining>0)SDL_Delay((uint32_t)remaining*1000.0);
}

bool is_termination_requested(){
	return termination_requested;
}

Image* load_image(const char* path){
	FILE* f=NULL;
	Image* img=NULL;
	const char* err=NULL;

	// Validate path / extension.
	if(!path){err="null path";goto fail;}
	const char* dot=strrchr(path,'.');
	if(!dot||strcmp(dot,".img")!=0){err="need .img";goto fail;}

	// Open and size the file.
	f=fopen(path,"rb");
	if(!f){err="open failed";goto fail;}
	fseek(f,0,SEEK_END);
	long file_size=ftell(f);
	rewind(f);
	if(file_size<8){err="too small";goto fail;}

	// Header: width, height (little-endian uint32).
	uint32_t w,h;
	fread(&w,sizeof(uint32_t),1,f);
	fread(&h,sizeof(uint32_t),1,f);
	if(w==0||h==0){err="bad size";goto fail;}
	size_t nbytes=(size_t)w*h*4;
	if(file_size<8+(long)nbytes){err="truncated";goto fail;}

	// Allocate image + pixel blob.
	img=malloc(sizeof(Image));
	if(!img){err="oom";goto fail;}
	img->width=w;
	img->height=h;
	img->pixels=malloc(nbytes);
	if(!img->pixels){err="oom";goto fail;}
	if(fread(img->pixels,1,nbytes,f)!=nbytes){err="read fail";goto fail;}

	fclose(f);
	return img;

fail:
	if(err)printf("ERROR: load_image %s: %s\n",path?path:"?",err);
	if(img){free(img->pixels);free(img);}
	if(f)fclose(f);
	return NULL;
}

uint32_t get_pixel(int x, int y){
	return pixels[x+WINDOW_WIDTH*y];
}

void draw_pixel(int x,int y,uint32_t color){

	//Clipping.
	if(x<0||x>=WINDOW_WIDTH||y<0||y>=WINDOW_HEIGHT)return;

	//Main.
	uint32_t sa=color&0xFF;
	if(sa==0)return;
	if(sa==255){
		pixels[x+WINDOW_WIDTH*y]=color;
		return;
	}

	//Alpha blending.
	uint32_t d=pixels[x+WINDOW_WIDTH*y];
	uint32_t sr=(color>>24)&0xFF;
	uint32_t sg=(color>>16)&0xFF;
	uint32_t sb=(color>>8)&0xFF;
	uint32_t dr=(d>>24)&0xFF;
	uint32_t dg=(d>>16)&0xFF;
	uint32_t db=(d>>8)&0xFF;
	uint32_t inv=(255-sa);
	uint32_t r=(sr*sa+dr*inv)/255;
	uint32_t g=(sg*sa+dg*inv)/255;
	uint32_t b=(sb*sa+db*inv)/255;
	pixels[x+WINDOW_WIDTH*y]=(r<<24)|(g<<16)|(b<<8)|0xFF;
}

void draw_line(int x0, int y0, int x1, int y1, uint32_t color_0, uint32_t color_1){
	int dx=abs(x1-x0);
	int sx=x0<x1?1:-1;
	int dy=-abs(y1-y0);
	int sy=y0<y1?1:-1;
	int error=dx+dy;
	int steps=dx>-dy?dx:-dy;
	int r0=(color_0>>24)&0xFF,g0=(color_0>>16)&0xFF,b0=(color_0>>8)&0xFF,a0=color_0&0xFF;
	int r1=(color_1>>24)&0xFF,g1=(color_1>>16)&0xFF,b1=(color_1>>8)&0xFF,a1=color_1&0xFF;
	int i=0;

	while (true){
		int r=steps?r0+(r1-r0)*i/steps:r0;
		int g=steps?g0+(g1-g0)*i/steps:g0;
		int b=steps?b0+(b1-b0)*i/steps:b0;
		int a=steps?a0+(a1-a0)*i/steps:a0;
		draw_pixel(x0,y0,((uint32_t)r<<24)|((uint32_t)g<<16)|((uint32_t)b<<8)|(uint32_t)a);

		if(x0==x1&&y0==y1)break;
		int e2=2*error;
		if(e2>=dy){
			error+=dy;
			x0+=sx;
		}
		if(e2<=dx){
			error+=dx;
			y0+=sy;
		}
		i++;
	}
}

