void draw_image(Image* image, int x, int y){
	for(int i=0;i<image->width;i++){
		for(int j=0;j<image->height;j++){
			draw_pixel(x+i,y+j,image->pixels[i+j*image->width]);
		}
	}
}

// Draw half-open region [rec_x0,rec_x1) x [rec_y0,rec_y1); (x,y) is source start.
// Inverted axis walks source and dest backwards on that axis.
// Source coords wrap modulo image size (negatives ok).
void draw_image_region(Image* image, int x, int y, int rec_x0, int rec_y0, int rec_x1, int rec_y1){
	if(!image||!image->pixels)return;
	int w=image->width;
	int h=image->height;
	if(w<=0||h<=0)return;

	int x_dir=(rec_x1>=rec_x0)?1:-1;
	int y_dir=(rec_y1>=rec_y0)?1:-1;
	int x_count=(rec_x1>=rec_x0)?(rec_x1-rec_x0):(rec_x0-rec_x1);
	int y_count=(rec_y1>=rec_y0)?(rec_y1-rec_y0):(rec_y0-rec_y1);

	for(int di=0;di<x_count;di++){
		for(int dj=0;dj<y_count;dj++){
			int sx=rec_x0+di*x_dir;
			int sy=rec_y0+dj*y_dir;
			int wx=sx%w;if(wx<0)wx+=w;
			int wy=sy%h;if(wy<0)wy+=h;
			// Dest steps with source direction (inverted range grows left/up).
			draw_pixel(x+di*x_dir,y+dj*y_dir,image->pixels[wx+wy*w]);
		}
	}
}


